# Data Type
'''
# List type data - note: [] = Square Brackets or Box Brackets use
x = ["Mango" , "Orange" , "Cherry"]
print(type(x))
print(x)
'''


'''
# Tuple type data - note: () = Parentheses or Round Bracket use
x = ("Tiger" , "Lion" , "Monky")
print(type(x))
print(x)
'''


'''
# Dictionary Data - note: {} = Braces or Curly Brackets use
x = {"name" : "Jamal" , "age" : 32}
print(type(x))
print(x)
'''


'''
# set type data -  note: {} = Braces or Curly Brackets use
x = {"Monir" , "Yeasin" , "Alomgir"}
print(type(x))
print(x)
'''


'''
# Frozenset type data - note: {} & () = Braces or Curly Brackets & Parentheses or Round Bracket use
x = frozenset({"Apple" , "Banana" , "Orange"})
print(type(x))
print(x)
'''


'''
# Boolean type data - conditional True / false
x = True
print(type(x))
print(x)
'''

'''
# None type data
x = None
print(type(x))
print(x)
'''


